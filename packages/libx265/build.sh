linuxdroid_step_pre_configure () {
	if [ $LINUXDROID_ARCH = "i686" ]; then
		# Avoid text relocations.
		LINUXDROID_PKG_EXTRA_CONFIGURE_ARGS="-DENABLE_ASSEMBLY=OFF"
	fi
	LINUXDROID_PKG_SRCDIR="$LINUXDROID_PKG_SRCDIR/source"
}

